#include <core/time/Time.h>
#include <hal/gpio/board.h>

using namespace avr_base;

int main(int _argc, char** _argv){
	_argc;
	_argv;

	hal::pin19 ledPin;

	Time::init();
	
	for (;;){
		ledPin.toggle();
		Time::delayMs(1000);
	}

	
}
